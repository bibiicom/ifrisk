DELIMITER $$
DROP PROCEDURE IF EXISTS create_partition_by_mon_11$$
CREATE PROCEDURE create_partition_by_month(IN_SCHEMANAME VARCHAR(64), IN_TABLENAME VARCHAR(64))
BEGIN
#目前日期，为当前月第一天
DECLARE TARGET_DATE TIMESTAMP;
#分区的名称，格式为p202203
DECLARE PARTITIONNAME VARCHAR(7);
#当前分区名称的分区值上限，即为 PARTITIONNAME + 1
DECLARE PARTITION_ADD_MON VARCHAR(11);
SELECT REPLACE(partition_name,'p','') INTO @P12_Name FROM INFORMATION_SCHEMA.PARTITIONS
WHERE table_schema = IN_SCHEMANAME AND table_name = IN_TABLENAME  ORDER BY partition_ordinal_position DESC LIMIT 1;
SET TARGET_DATE= CONCAT(@P12_Name,'01');
WHILE TARGET_DATE < last_day(curdate()) DO
SET TARGET_DATE = date_format(date_add(TARGET_DATE,interval 1 month),'%Y%m%d');
SET PARTITIONNAME = DATE_FORMAT( TARGET_DATE, 'p%Y%m' );
SET PARTITION_ADD_MON = DATE_FORMAT( date_add(TARGET_DATE,interval 1 month), '%Y-%m-%d' );
SET @SQL = CONCAT( 'ALTER TABLE ', IN_SCHEMANAME, '.', IN_TABLENAME, '',
' ADD PARTITION (PARTITION ', PARTITIONNAME, " VALUES LESS THAN ( '",
PARTITION_ADD_MON ,"') ENGINE = InnoDB);" );
PREPARE STMT FROM @SQL;
EXECUTE STMT;
DEALLOCATE PREPARE STMT;
END WHILE;
SELECT CONCAT("Add partition ", " for table ",IN_SCHEMANAME, ".", IN_TABLENAME, "  complete ！") AS result;
END$$
